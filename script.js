// Z√≠sk√° datum bez ƒçasu (YYYY-MM-DD), aby se dala porovn√°vat jen data
function getTodayDateString() {
  const now = new Date();
  return now.toISOString().split("T")[0];
}

// Zaktualizuje poƒçet p≈ôidan√Ωch produkt≈Ø za dne≈°n√≠ den
function updateDailyCountDisplay() {
  const products = JSON.parse(localStorage.getItem("products")) || [];
  const todayStr = getTodayDateString();
  let dailyCount = 0;
  products.forEach((p) => {
    if (p.dateAdded === todayStr) {
      dailyCount++;
    }
  });
  dailyCountElem.textContent = `(Dnes p≈ôid√°no: ${dailyCount} produkt≈Ø)`;
}

