/* ------------------------
   Nahr√°n√≠ souboru na Cloudinary
-------------------------*/
async function uploadFile(file) {
  const formData = new FormData();
  formData.append("file", file);
  formData.append("upload_preset", CLOUDINARY_UPLOAD_PRESET);
  // Obr√°zky do slo≈æky "media_library"
  formData.append("folder", file.type.includes("image") ? "media_library" : "others");

  const resp = await fetch(CLOUDINARY_UPLOAD_URL, {
    method: "POST",
    body: formData
  });

  if (!resp.ok) {
    const errorText = await resp.text();
    throw new Error(`Chyba p≈ôi nahr√°v√°n√≠: ${resp.status} - ${errorText}`);
  }

  const data = await resp.json();
  return data.secure_url;
}

